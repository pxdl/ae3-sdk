/* Expand the kernel into the instance's phase-major [256][4] form. Gaussian uses
 * the hardware's exact index pattern (see the header comment) so the tick's four
 * multiply-shift-adds read the same values in the same order as indexing ae3_gauss
 * directly -- bit-identical output. The bright alternative is catmull-rom evaluated
 * at f = i/256 (interpolating win[1] -> win[2], matching the gaussian's phase
 * convention: at i=0 the heavy tap is win[1]), scaled to the gaussian table's own
 * 0x7F80 DC gain so switching kernels does not change loudness. */
#include <math.h>

void ae3__interp_build(ae3_synth *s)
{
    for (int i = 0; i < 256; i++) {
        if (s->gaussian) {
            s->interp[i][0] = ae3_gauss[0x0FF - i];
            s->interp[i][1] = ae3_gauss[0x1FF - i];
            s->interp[i][2] = ae3_gauss[0x100 + i];
            s->interp[i][3] = ae3_gauss[0x000 + i];
        } else {
            double f = i / 256.0, f2 = f * f, f3 = f2 * f;
            s->interp[i][0] = (int16_t)lround(0.5 * (-f3 + 2 * f2 - f) * 0x7F80);
            s->interp[i][1] = (int16_t)lround(0.5 * (3 * f3 - 5 * f2 + 2) * 0x7F80);
            s->interp[i][2] = (int16_t)lround(0.5 * (-3 * f3 + 4 * f2 + f) * 0x7F80);
            s->interp[i][3] = (int16_t)lround(0.5 * (f3 - f2) * 0x7F80);
        }
    }
}
